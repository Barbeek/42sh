#include "shell.h"

int		is_assignement(char *s)
{
	if (*s != '_' && !ft_isalpha(*s))
		return (0);
	++s;
	while (*s && *s != ' ')
	{
		if (*s == '=')
			return (1);
		if (*s != '_' && !ft_isalnum(*s))
			return (0);
		++s;
	}
	return (0);
}
